#include "push_swap.h"

void	rb(t_stack *b)
{
	int	tmp;
	int	i;

	tmp = b->tab[b->len - 1];
	i = b->len - 1;
	while (i > 0)
	{
		b->tab[i] = b->tab[i - 1];
		i--;
	}
	b->tab[0] = tmp;
	ft_putstr("rb\n");
}
