#include "push_swap.h"

void	pb(t_stack *a, t_stack *b)
{
	if (a->len == 0)
		return ;
	b->len++;
	b->tab[b->len - 1] = a->tab[a->len - 1];
	a->len--;
	ft_putstr("pb\n");
}
