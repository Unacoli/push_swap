#include "push_swap.h"

void	pa(t_stack *a, t_stack *b)
{
	if (b->len == 0)
		return ;
	a->len++;
	a->tab[a->len - 1] = b->tab[b->len - 1];
	b->len--;
	ft_putstr("pa\n");
}
